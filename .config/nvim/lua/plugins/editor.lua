				{ "<leader>r", icon = { icon = "󰸱 ", color = "purple" } },
				{ "<leader>t", group = "Tabs", icon = { icon = "󰓩 ", color = "cyan" } },
			},
		},
	},

	{
		"folke/flash.nvim",
		enabled = false,
	},

	{
		"brenoprata10/nvim-highlight-colors",
		event = "BufReadPre",
		opts = {
			render = "background",
			enable_hex = true,
			enable_short_hex = true,
